/**
 * The prefix the bot will look for before executing named commands.
 * E.g. if set to _ the bot will check for the info command using _info.
 * Default is underscore.
 */
export const COMMAND_PRFEIX = '_';
/**
 * The prefix the bot will look for collecting command flags.
 * E.g. if set to + the bot will check for flags using +reportedby.
 * Default is plus sign.
 */
export const FLAG_PREFIX = '+';
